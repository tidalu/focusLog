import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { format } from 'prettier';

const checkOnly = process.argv.includes('--check');
const openApiPath = resolve('contracts/openapi/focuslog-v1.json');
const websocketPath = resolve('contracts/events/focuslog-ws-v1.schema.json');
const backendRoutesPath = resolve('apps/backend/src/routes/v1.ts');
const gatewayPath = resolve('apps/backend/src/services/websocket-gateway.ts');
const typescriptOutput = resolve('packages/shared-types/src/generated-contracts.ts');
const dartOutput = resolve('apps/mobile/lib/generated/contracts.dart');

const openapi = JSON.parse(readFileSync(openApiPath, 'utf8'));
const websocket = JSON.parse(readFileSync(websocketPath, 'utf8'));
const backendSource = readFileSync(backendRoutesPath, 'utf8');
const gatewaySource = readFileSync(gatewayPath, 'utf8');
const methods = new Set(['get', 'post', 'patch', 'delete', 'put']);
const documented = new Set();
const operations = [];
for (const [path, item] of Object.entries(openapi.paths)) {
  for (const [method, operation] of Object.entries(item)) {
    if (!methods.has(method)) continue;
    documented.add(`${method.toUpperCase()} ${path}`);
    if (!operation.operationId)
      throw new Error(`OpenAPI operation ${method.toUpperCase()} ${path} has no operationId.`);
    operations.push(operation.operationId);
  }
}
const implemented = new Set(
  [...backendSource.matchAll(/app\.(get|post|patch|delete)\(\s*['`]([^'`]+)['`]/g)].map(
    (match) => `${match[1].toUpperCase()} ${match[2].replace(/:([A-Za-z][A-Za-z0-9]*)/g, '{$1}')}`
  )
);
const missing = [...implemented].filter((route) => !documented.has(route));
const stale = [...documented].filter((route) => !implemented.has(route));
if (missing.length || stale.length)
  throw new Error(
    `OpenAPI drift detected. Missing: ${missing.join(', ') || 'none'}. Stale: ${
      stale.join(', ') || 'none'
    }.`
  );

const clientTypes = websocket['x-focuslog-client-message-types'];
const serverTypes = websocket['x-focuslog-server-message-types'];
for (const eventType of [...clientTypes, ...serverTypes])
  if (!gatewaySource.includes(`'${eventType}'`))
    throw new Error(`WebSocket gateway does not implement contracted event ${eventType}.`);

const quotedUnion = (values) => values.map((value) => JSON.stringify(value)).join(' | ');
const typescriptSource = `// Generated by scripts/generate-contracts.mjs. Do not edit.
export const restApiVersion = ${JSON.stringify(openapi.info.version)} as const;
export const websocketProtocolVersion = '1' as const;
export const restOperationIds = ${JSON.stringify(operations, null, 2)} as const;
export type RestOperationId = (typeof restOperationIds)[number];
export type ClientWebSocketMessageType = ${quotedUnion(clientTypes)};
export type ServerWebSocketMessageType = ${quotedUnion(serverTypes)};
export interface WebSocketClientFrame<T extends ClientWebSocketMessageType = ClientWebSocketMessageType> {
  readonly version: typeof websocketProtocolVersion;
  readonly type: T;
  readonly requestId: string;
  readonly sentAt: string;
  readonly payload: Readonly<Record<string, unknown>>;
}
export interface WebSocketServerFrame<T extends ServerWebSocketMessageType = ServerWebSocketMessageType> {
  readonly version: typeof websocketProtocolVersion;
  readonly type: T;
  readonly eventId: string;
  readonly requestId?: string;
  readonly sentAt: string;
  readonly payload: Readonly<Record<string, unknown>>;
}
`;
const typescript = await format(typescriptSource, {
  parser: 'typescript',
  singleQuote: true,
  trailingComma: 'none',
  printWidth: 100
});

const dartName = (value) =>
  value.replace(/[.-]([a-z])/g, (_, character) => character.toUpperCase());
const dartEnum = (name, values) => `enum ${name} {
${values.map((value) => `  ${dartName(value)}(${JSON.stringify(value)})`).join(',\n')};

  const ${name}(this.wireName);
  final String wireName;
}
`;
const dart =
  `// Generated by scripts/generate-contracts.mjs. Do not edit.
const restApiVersion = ${JSON.stringify(openapi.info.version)};
const websocketProtocolVersion = '1';

${dartEnum('ClientWebSocketMessageType', clientTypes)}
${dartEnum('ServerWebSocketMessageType', serverTypes)}
`.trimEnd() + '\n';

function emit(path, content) {
  if (checkOnly) {
    let current;
    try {
      current = readFileSync(path, 'utf8');
    } catch {
      throw new Error(`Generated contract is missing: ${path}`);
    }
    if (current !== content) throw new Error(`Generated contract is stale: ${path}`);
  } else {
    writeFileSync(path, content);
  }
}

emit(typescriptOutput, typescript);
emit(dartOutput, dart);
console.log(
  `Contract ${checkOnly ? 'verification' : 'generation'} passed: ${documented.size} REST operations, ${clientTypes.length + serverTypes.length} WebSocket messages.`
);
